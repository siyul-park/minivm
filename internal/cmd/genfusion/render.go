func testRuleKind(rule rule) (string, error) {
	for i := len(rule.pattern) - 1; i >= 0; i-- {
		switch rule.pattern[i].op {
		case instr.ARRAY_GET, instr.STRUCT_GET:
			return "KindI32", nil
		}
		if kind, ok := numericKind(rule.pattern[i].op); ok {
			return "Kind" + kind, nil
		}
	}
	for _, operation := range rule.pattern {
		switch operation.op {
		case instr.LOCAL_GET, instr.GLOBAL_GET, instr.UPVAL_GET, instr.CONST_GET, instr.REF_NULL, instr.DUP, instr.CALL, instr.RETURN_CALL, instr.CLOSURE_NEW:
			return "KindRef", nil
		}
	}
	return "", fmt.Errorf("no test kind for %s", patternKey(rule.pattern))
}

func testRuleName(rule rule) string {
	parts := make([]string, len(rule.pattern))
	for i, operation := range rule.pattern {
		parts[i] = instr.TypeOf(operation.op).Mnemonic
		if operation.guard != nil {
			if operation.guard.negations == 1 {
				parts[i] += "[not " + operation.guard.typeOf.String() + "]"
			} else {
				parts[i] += "[" + operation.guard.typeOf.String() + "]"
			}
		}
	}
	return strings.Join(parts, "/")
}

func testOpcodes(pattern []operation) string {
	parts := make([]string, len(pattern))
	for i, operation := range pattern {
		parts[i] = "instr." + opcodeName(operation.op)
	}
	return strings.Join(parts, ", ")
}

func renderThreaded(rules []rule) ([]byte, error) {
	file := jen.NewFile("interp")
	file.HeaderComment("Code generated by internal/cmd/genfusion; DO NOT EDIT.")

	var groups [256][]rule
	for _, rule := range rules {
		groups[rule.pattern[0].op] = append(groups[rule.pattern[0].op], rule)
	}

	var cases []jen.Code
	nextLabel := 0
	for opcode, rules := range groups {
		if len(rules) == 0 {
			continue
		}
		var body []jen.Code
		for _, rule := range rules {
			label := fmt.Sprintf("l%d", nextLabel)
			nextLabel++
			total := patternWidth(rule.pattern)
			statements, err := renderThreadedRule(rule, total, label)
			if err != nil {
				return nil, err
			}
			if rule.pattern[len(rule.pattern)-1].op == instr.BR_IF {
				offset := total - opcodeWidth(instr.BR_IF) + 1
				statements = append([]jen.Code{jen.Id("offset").Op(":=").Qual("github.com/siyul-park/minivm/instr", "ParseI16").Call(jen.Id("c").Dot("code"), jen.Id("start").Op("+").Lit(offset))}, statements...)
			}
			body = append(body, jen.If(jen.Op("!").Parens(renderPattern(rule.pattern))).Block(jen.Goto().Id(label)))
			body = append(body, jen.Block(statements...))
			body = append(body, jen.Id(label).Op(":"))
		}
		body = append(body, jen.Break())
		cases = append(cases, jen.Case(jen.Qual("github.com/siyul-park/minivm/instr", opcodeName(instr.Opcode(opcode)))).Block(body...))
	}

	body := []jen.Code{
		jen.Id("start").Op(":=").Id("c").Dot("ip"),
		jen.If(jen.Id("start").Op(">=").Len(jen.Id("c").Dot("code"))).Block(jen.Return(jen.Nil())),
		jen.Switch(jen.Qual("github.com/siyul-park/minivm/instr", "Opcode").Call(jen.Id("c").Dot("code").Index(jen.Id("start")))).Block(cases...),
		jen.Return(jen.Nil()),
	}
	file.Func().Params(jen.Id("c").Op("*").Id("threader")).Id("fusion").Params().Func().Params(jen.Op("*").Id("Interpreter")).Block(body...)
	return formatGenerated([]byte(file.GoString()))
}

func reject(label string) jen.Code {
	if label != "" {
		return jen.Goto().Id(label)
	}
	return jen.Return(jen.Nil())
}

func renderThreadedRule(rule rule, total int, label string) ([]jen.Code, error) {
	last := rule.pattern[len(rule.pattern)-1].op
	consumer := last
	if last == instr.BR_IF && len(rule.pattern) > 1 {
		consumer = rule.pattern[len(rule.pattern)-2].op
	}
	if consumer == instr.CALL || consumer == instr.RETURN_CALL || consumer == instr.CLOSURE_NEW {
		return renderCall(rule, label)
	}
	if consumer == instr.DROP || consumer == instr.REF_IS_NULL {
		return renderRefFusion(rule, total, label)
	}
	if consumer == instr.ARRAY_GET || consumer == instr.STRUCT_GET {
		return renderThreadedIndex(rule, total, label)
	}
	if len(rule.pattern) == 2 && rule.pattern[0].op == instr.I32_CONST && last == instr.BR_IF {
		return renderI32ConstBranch(total), nil
	}
	if _, _, ok := numericConsumer(consumer); ok {
		return renderNumericFusion(rule, total, label)
	}
	return nil, fmt.Errorf("no threaded renderer for %s", instr.TypeOf(consumer).Mnemonic)
}

func renderI32ConstBranch(total int) []jen.Code {
	return []jen.Code{
		jen.Id("value").Op(":=").Add(numericImmediate("I32", jen.Id("start"))),
		jen.Id("c").Dot("ip").Op("+=").Lit(opcodeWidth(instr.I32_CONST)),
		jen.Return(jen.Func().Params(jen.Id("i").Op("*").Id("Interpreter")).Block(
			jen.If(jen.Id("i").Dot("sp").Op("==").Len(jen.Id("i").Dot("stack"))).Block(jen.Panic(jen.Id("ErrStackOverflow"))),
			jen.Id("i").Dot("branchIf").Call(jen.Id("value").Op("!=").Lit(0), jen.Id("offset"), jen.Lit(total)),
		)),
	}
}

func renderPattern(pattern []operation) jen.Code {
	total := patternWidth(pattern)
	condition := jen.Id("start").Op("+").Lit(total).Op("<=").Len(jen.Id("c").Dot("code"))
	offset := opcodeWidth(pattern[0].op)
	for _, operation := range pattern[1:] {
		condition = condition.Op("&&").Qual("github.com/siyul-park/minivm/instr", "Opcode").Call(jen.Id("c").Dot("code").Index(add(jen.Id("start"), offset))).Op("==").Qual("github.com/siyul-park/minivm/instr", opcodeName(operation.op))
		offset += opcodeWidth(operation.op)
	}
	return condition
}

func opcodeName(op instr.Opcode) string {
	return strings.ToUpper(strings.ReplaceAll(instr.TypeOf(op).Mnemonic, ".", "_"))
}

func patternWidth(pattern []operation) int {
	total := 0
	for _, operation := range pattern {
		total += opcodeWidth(operation.op)
	}
	return total
}

// renderARM64 renders the ARM64-marked concrete rules. The emitted matcher
// selects a declared specialization before any lowering state is mutated.
func renderARM64(rules []rule) ([]byte, error) {
	arm64 := make([]rule, 0, len(rules))
	for _, rule := range rules {
		if rule.arm64 {
			arm64 = append(arm64, rule)
		}
	}

	file := jen.NewFile("interp")
	file.HeaderComment("Code generated by internal/cmd/genfusion; DO NOT EDIT.")
	file.Func().Params(jen.Id("l").Id("arm64Lowerer")).Id("fusion").Params(
		jen.Id("ctx").Op("*").Id("lowering"),
		jen.Id("ops"), jen.Id("tail").Index().Id("step"),
		jen.Id("idx").Int(),
	).Params(jen.Int(), jen.Bool()).Block(arm64FusionBody()...)
	file.Line()
	file.Func().Params(jen.Id("l").Id("arm64Lowerer")).Id("match").Params(
		jen.Id("ctx").Op("*").Id("lowering"),
		jen.Id("ops").Index().Id("step"),
		jen.Id("idx").Int(),
	).Int().Block(arm64MatchBody(arm64)...)
	file.Line()
	file.Func().Params(jen.Id("l").Id("arm64Lowerer")).Id("adjacent").Params(jen.Id("a"), jen.Id("b").Id("step")).Bool().Block(
		jen.If(jen.Id("a").Dot("fn").Op("!=").Id("b").Dot("fn").Op("||").Id("a").Dot("depth").Op("!=").Id("b").Dot("depth")).Block(jen.Return(jen.False())),
		jen.Id("width").Op(":=").Lit(1),
		jen.For(jen.List(jen.Id("_"), jen.Id("operand")).Op(":=").Range().Qual("github.com/siyul-park/minivm/instr", "TypeOf").Call(jen.Id("a").Dot("op")).Dot("Widths")).Block(
			jen.If(jen.Id("operand").Op("<").Lit(0)).Block(jen.Return(jen.False())),
			jen.Id("width").Op("+=").Id("operand"),
		),
		jen.Return(jen.Id("b").Dot("ip").Op("==").Id("a").Dot("ip").Op("+").Id("width")),
	)
	return formatARM64(file)
}

func arm64FusionBody() []jen.Code {
	return []jen.Code{
		jen.Id("v0").Op(":=").Id("l").Dot("match").Call(jen.Id("ctx"), jen.Id("ops"), jen.Id("idx")),
		jen.If(jen.Id("v0").Op("==").Lit(0)).Block(jen.Return(jen.Lit(0), jen.True())),
		jen.Id("r0").Op(":=").Id("ops").Index(jen.Id("idx")),
		jen.Id("r1").Op(":=").Id("ops").Index(jen.Id("idx").Op("+").Lit(1)),
		jen.If(jen.Id("r0").Dot("op").Op("==").Qual("github.com/siyul-park/minivm/instr", "CONST_GET").Op("&&").Parens(
			jen.Id("r1").Dot("op").Op("==").Qual("github.com/siyul-park/minivm/instr", "CALL").Op("||").Id("r1").Dot("op").Op("==").Qual("github.com/siyul-park/minivm/instr", "RETURN_CALL"),
		)).Block(arm64ConstantCall()...),
		jen.Id("r2").Op(":=").Id("step").Values(),
		jen.If(jen.Id("v0").Op("==").Lit(3)).Block(jen.Id("r2").Op("=").Id("ops").Index(jen.Id("idx").Op("+").Lit(2))),
		jen.Id("f").Op(":=").Id("ctx").Dot("frame").Call(),
		jen.Var().Defs(
			jen.Id("i0").Int(), jen.Id("i1").Int(), jen.Id("i2").Int(), jen.Id("i3").Int(), jen.Id("i4").Int(),
			jen.Id("r3").Qual("github.com/siyul-park/minivm/types", "Boxed"),
		),
		jen.Switch(jen.Id("r0").Dot("op")).Block(arm64RefPreflightCases()...),
		jen.Id("r4").Op(":=").Qual("github.com/siyul-park/minivm/asm", "VReg").Values(),
		jen.Switch(jen.Id("r0").Dot("op")).Block(arm64RefCases()...),
		jen.Id("r5").Op(":=").Id("ctx").Dot("assembler").Dot("Reg").Call(jen.Qual("github.com/siyul-park/minivm/asm", "RegTypeInt"), jen.Qual("github.com/siyul-park/minivm/asm", "Width64")),
		jen.Id("r6").Op(":=").Id("ctx").Dot("assembler").Dot("Reg").Call(jen.Qual("github.com/siyul-park/minivm/asm", "RegTypeInt"), jen.Qual("github.com/siyul-park/minivm/asm", "Width64")),
		jen.Id("ctx").Dot("assembler").Dot("Emit").Call(jen.Qual("github.com/siyul-park/minivm/asm/arm64", "LDI").Call(jen.Id("r6"), jen.Uint64().Parens(jen.Qual("github.com/siyul-park/minivm/types", "BoxedNull"))).Op("...")),
		jen.Id("ctx").Dot("assembler").Dot("Emit").Call(jen.Qual("github.com/siyul-park/minivm/asm/arm64", "CMP").Call(jen.Id("r4"), jen.Id("r6")), jen.Qual("github.com/siyul-park/minivm/asm/arm64", "CSET").Call(jen.Id("r5"), jen.Qual("github.com/siyul-park/minivm/asm/arm64", "CondEQ"))),
		jen.Id("ctx").Dot("push").Call(jen.Id("value").Values(jen.Dict{jen.Id("reg"): jen.Id("r5"), jen.Id("kind"): jen.Qual("github.com/siyul-park/minivm/types", "KindI1"), jen.Id("raw"): jen.True()})),
		jen.If(jen.Id("v0").Op("==").Lit(3).Op("&&").Op("!").Id("l").Dot("brIf").Call(jen.Id("ctx"), jen.Id("r2"), jen.Id("l").Dot("callerTail").Call(jen.Id("ops"), jen.Id("idx").Op("+").Lit(2), jen.Id("tail")))).Block(jen.Return(jen.Lit(0), jen.False())),
		jen.Return(jen.Id("v0"), jen.True()),
	}
}

func arm64MatchBody(rules []rule) []jen.Code {
	return []jen.Code{
		jen.If(jen.Id("idx").Op("+").Lit(1).Op(">=").Len(jen.Id("ops")).Op("||").Op("!").Id("l").Dot("adjacent").Call(jen.Id("ops").Index(jen.Id("idx")), jen.Id("ops").Index(jen.Id("idx").Op("+").Lit(1)))).Block(jen.Return(jen.Lit(0))),
		jen.Id("r0").Op(":=").Id("ops").Index(jen.Id("idx")),
		jen.Id("r1").Op(":=").Id("ops").Index(jen.Id("idx").Op("+").Lit(1)),
		jen.Var().Id("v0").Int(),
		jen.Switch(jen.Uint16().Parens(jen.Id("r0").Dot("op")).Op("<<").Lit(8).Op("|").Uint16().Parens(jen.Id("r1").Dot("op"))).Block(arm64Cases(rules)...),
		jen.Return(jen.Id("v0")),
	}
}

func arm64Cases(rules []rule) []jen.Code {
	type pairRule struct {
		base      bool
		extension instr.Opcode
		extended  bool
	}

	var pairs [][2]instr.Opcode
	matched := make(map[[2]instr.Opcode]pairRule)
	for _, rule := range rules {
		pair := [2]instr.Opcode{rule.pattern[0].op, rule.pattern[1].op}
		match, ok := matched[pair]
		if !ok {
			pairs = append(pairs, pair)
		}
		if len(rule.pattern) == 2 {
			match.base = true
		} else {
			match.extension = rule.pattern[2].op
			match.extended = true
		}
		matched[pair] = match
	}

	cases := make([]jen.Code, 0, len(pairs))
	for _, pair := range pairs {
		match := matched[pair]
		var body []jen.Code
		if match.base {
			body = append(body, jen.Id("v0").Op("=").Lit(2))
		}
		if match.extended {
			next := jen.Id("ops").Index(jen.Id("idx").Op("+").Lit(2))
			body = append(body, jen.If(
				jen.Id("idx").Op("+").Lit(2).Op("<").Len(jen.Id("ops")).Op("&&").
					Id("l").Dot("adjacent").Call(jen.Id("r1"), next).Op("&&").
					Add(next).Dot("op").Op("==").Qual("github.com/siyul-park/minivm/instr", opcodeName(match.extension)).Op("&&").
					Parens(jen.Op("!").Id("ctx").Dot("loop").Op("||").Add(next).Dot("target").Op("!=").Id("ctx").Dot("tree").Dot("root").Dot("anchor").Dot("ip")),
			).Block(jen.Id("v0").Op("=").Lit(3)))
		}
		key := jen.Uint16().Parens(jen.Qual("github.com/siyul-park/minivm/instr", opcodeName(pair[0]))).Op("<<").Lit(8).Op("|").Uint16().Parens(jen.Qual("github.com/siyul-park/minivm/instr", opcodeName(pair[1])))
		cases = append(cases, jen.Case(key).Block(body...))
	}
	return cases
}

// arm64RefPreflightCases emits only reads and local scalar assignments. It is
// the selection boundary: no register, IR, symbolic stack, fact, label, exit,
// or journal state may be changed before this switch accepts a candidate.
